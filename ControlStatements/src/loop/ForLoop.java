package loop;

public class ForLoop {
	public static void main(String[] args) {
		for(int i=0;i<=3;i++) {
			System.out.println("The Value of i is " +i);
			
		}
		System.out.println("--------------------------------");
		for(int i=0;i<=3;++i) {
			System.out.println("The Value of i is " +i);
		}
	}

}
